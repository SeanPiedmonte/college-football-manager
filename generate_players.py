def generate_stat_values(mean, std_dev, size):
    return  np.random.normal(mean, std_dev, size)

def loop_stat_list(stats, std_devs, size):
    return np.array([generate_stat_values(s, std, size) for s, std in zip(stats, std_devs)])

def generate_roster(num_teams):
    roster_file = {}
    for i in range(num_teams):
        roster_file[i] = {pos: loop_stat_list(cfg["mean"], cfg["std"], cfg["size"])
                          for pos, cfg in stats.items()}
    return roster_file

